// Populating Arrays
let clothes = []

// Add clothes
clothes.push("cap", "Tshirt", "Jeans", "sneakers", "belt")
console.log(clothes)

// Remove
clothes.pop()
console.log(clothes)

clothes.push("socks")
console.log(clothes)

// Show the third item in the array
console.log(`The third item in the array is: ${clothes[2]}`)

